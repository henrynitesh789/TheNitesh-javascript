
/* Syntax: while loop

    while (condition) {
        ----------
    }
*/
let i = 0
while (i <= 2){
    console.log(i);
    i++;
}

/* Syntax: do-while loop

    do {
        -----------
    } while (condition);
*/
let score = 11
do {
    console.log("score is", score);
    score++
} while (score < 4);